#basic arithmetic
import numpy as np

array_1 = np.array([[1, 2], [4, 5]])
array_2 = np.array([[7, 8], [10, 11]])

addition = array_1 + array_2
subtraction = array_1 - array_2
multiplication = array_1 * array_2
division = array_1 / array_2

print(addition)
print(subtraction)
print(multiplication)
print(division)